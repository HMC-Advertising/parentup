
<div>
    <h3>Community Values Youth <small> Young person perceives that adults in the community value youth.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong><ul><li>
When young workers at a grocery store, drugstore, or fast-food restaurant wait on you, greet them in a friendly manner and compliment them on something (their good work, their unusual hairstyle).</li><li>
Be patient with young workers! Don’t show irritation if they make a mistake.</li><li>
Celebrate a young employee’s new job with a lunch date and a tour of your workplace. Talk about your job and the job he or she has been hired to do. Encourage lots of questions.</li></ul><p>
*For more on this topic, see <em>Empowering Youth: How to Encourage Young Leaders to Do Great Things</em> by Kelly Curtis.</p></div></div><div class="page-break"></div>
