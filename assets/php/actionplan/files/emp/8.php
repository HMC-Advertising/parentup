    <h3>Youth as Resources <small> Young people are given useful roles in the community. </small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><p>

Solicit young people’s input in all decisions that affect them. If you’re on a decision-making board, invite young people to be members—and then really listen to what they have to say.</p><p>

If you’re in charge of a fundraising or charity event, involve your children or students. They will learn by watching you in action, but they will learn even more if they’re given a meaningful task to complete.</p><p>

Encourage kids to mentor their peers. Teach them how they can help other youth by listening to them and helping them work through their problems.</p><p>

*For more on this topic, see Empowering Youth: How to Encourage Young Leaders to do Great Things by Kelly Curtis.</p></div></div><div class="page-break"></div>
