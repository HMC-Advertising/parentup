<div>
    <h3>Service to Others <small> Young person serves in the community one hour or more per week. </small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><p>
Together with your kids, do something for someone else, whether it’s making a financial contribution, baking cookies, or helping someone out.</p><p>
Make and send cards to hospitalized children, nursing home residents, or people in the military.</p><p>
Organize a community or neighborhood “closet-cleaning day.” Deliver everything you collect to a shelter or thrift store.</p><p>
Provide foster care for a pet through an animal shelter or for a friend or neighbor who is out of town or ill.</p><p>
Organize or participate together in a fundraiser such as a walk or run. Donate the proceeds to hurricane relief, camp scholarships, or other causes.</p><p>
*For more on this topic, see Empowering Youth: How to Encourage Young Leaders to do Great Things by Kelly Curtis and The Best of Building Assets Together: Favorite Group Activities That Help Youth Succeed by Jolene Roehlkepartain.</p></div></div>
