<div>
    <h3>Service to Others <small> Young person serves in the community one hour or more per week. </small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><ul><li>
Together with your kids, do something for someone else, whether it’s making a financial contribution, baking cookies, or helping someone out.</li><li>
Make and send cards to hospitalized children, nursing-home residents, or people in the military.</li><li>
Organize a community or neighborhood “closet-cleaning day.” Deliver everything you collect to a shelter or thrift store.</li><li>
Provide foster care for a pet through an animal shelter or for a friend or neighbor who is out of town or ill.</li><li>
Organize or participate together in a fund-raiser such as a walk or run. Donate the proceeds to hurricane relief, camp scholarships, or other causes.</li></ul><p>
*For more on this topic, see <em>Empowering Youth: How to Encourage Young Leaders to Do Great Things</em> by Kelly Curtis and <em>The Best of Building Assets Together: Favorite Group Activities that Help Youth Succeed</em> by Jolene Roehlkepartain.</p></div></div>
