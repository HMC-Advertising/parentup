    <h3>Neighborhood Boundaries<small> Neighbors take responsibility for monitoring young people’s behavior.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><p>
Tell other parents when you see their children being responsible or generous in their actions. Try to find opportunities to praise more often than you report misbehavior.</p><p>
Make your home one that kids want to come to. If kids get rowdy in your home, be calm but firm in re-establishing order.</p><p>
Meet the parents of your children’s friends. If your preteen wants to go with friends to a movie or the mall without you, call other parents and agree on pick-up times and movie choices.</p><p>
*For more on this topic, see Parenting Preteens with a Purpose: Navigating the Middle Years by Kate Thomsen and The Best of Building Assets Together: Favorite Activities That Help Youth Succeed by Jolene Roehlkepartain.</p></div></div><div class="page-break"></div>
