<div>
    <h3>Bonding to School<small> Young person cares about her or his school.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><ul><li>
If you are able, purchase school t-shirts, caps, sweatshirts, or other school clothing that’s for sale. Wearing these items helps children show pride in their school.</li><li>
Participate with your child in service projects, such as food drives, conducted by the school. Invite one of your child’s friends to join you.</li><li>
Show that you care about your child’s school. Join a parent-teacher organization, attend conferences and special events, and volunteer in any way you can.</li><li>
Listen to your teenager when he complains about school or talks about not feeling connected. Is there a specific problem?</li><li>
Don’t forget to identify on caring adult at school as your family’s “ally.” Aim for at least five caring adults in your teen’s life.</li></ul><p>
*For more on this topic, see <em>Engage Every Parent! Encouraging Families to Sign On, Show Up, and Make a Difference</em> by Nancy Tellett-Royce and Susan Wootten.</p></div></div><div class="page-break"></div>
