<div>
    <h3>Reading for Pleasure<small> Young person reads for pleasure three or more hours per week.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><ul><li>
Bring a young person to look for books at garage sales, rummage sales, and second-hand stores.</li><li>
Ask children to read to you as they learn to read. Show them that you are excited and proud about their reading.</li><li>
Ask a teenager to recommend a favorite book. Read the book and start a discussion later about the character’s values.</li><li>
Give your child books and magazine subscriptions as birthday and holiday presents.</li><li>
Make it a family ritual to read together in the evening—with the television, cell phones, and computers turned off!</li></ul><p>
*For more on this topic, see <em>The Best of Building Assets Together: Favorite Group Activities that Help Youth Succeed</em> by Jolene L. Roehlkepartain.</p></div></div><div class="page-break"></div>
