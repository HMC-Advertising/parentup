    <h3>Creative Activities<small> Young person spends three or more hours per week in lessons or practice in music, theater, or other arts.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><ul><li>
Hand down a hobby. Teach a young person a skill, such as quilting, carpentry, or gardening.</li><li>
Help your children—at every age—find positive outlets for their creative energy. This might include classes, crafts, physical activities, drama, or more.</li><li>
If you played an instrument when you were younger, take a refresher course. Then set a good example and practice often. Or join a choir, try out for a play, pick up a paintbrush, or write a poem. Share your excitement with your children.</li></ul><p>
*For more on this topic, see The Best of Building Assets Together: Favorite Group Activities That Help Youth Succeed by Jolene Roehlkepartain.</p></div></div><div class="page-break"></div>
