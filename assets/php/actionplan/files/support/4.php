<div>
    <h3>Caring Neighborhood<small> Young person experiences caring neighbors.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><p>

Have a neighborhood celebration on the first or last day of the school year. Invite youth, parents, teachers, and other neighbors.</p><p>
Organize formal activities (such as pick-up basketball) for young people in your neighborhood. Make plans to do the activity weekly if they are interested.</p><p>
If you live in an apartment or condominium, spend time in gathering places, such as front steps, courtyards, meeting rooms, pools, laundry rooms, and lobbies. Greet people and try to start conversations.</p><p>
Let the kids in your neighborhood know they can play basketball in your driveway, cut through your yard to get to school, sled down the hill in your backyard—whatever you feel comfortable with.</p><p>
Organize a neighborhood bake sale or garage sale, or try a barter day—you and your neighbors can gather to trade items.</p><p>
*For more on this topic, see Tag, You’re It! 50 Ways to Connect with Young People by Kathleen Kimball-Baker.</p></div></div>
