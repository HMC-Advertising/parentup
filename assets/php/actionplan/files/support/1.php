
<div>
    <h3>Family Support <br> <small> Family life provides high levels of love and support</small></h3>
    <div class="content">
    <p><strong>
How to Take Action:</strong></p>
<ul>
<li>
Start family traditions and rituals such as family service, game nights, season outings, or family meetings.</li><li>
Give kids space and respect their privacy when they need it.</li><li>
Give each of your kids a hug today, even if they’re really big kids.</li><li>
Spend time each week with each of your teenagers individually.</li><li>
Create a small memory book, memory box, photo album, or private Web site for each of your children.</li><li>
If you don’t live in the same city as your child, create a care package that includes a pack of cards; a book of crossword puzzles, word jumbles, or drawing activities; and some colorful pens, pencils, or markers.</li></ul>
<p style="padding-top:20px;">*For more on this topic, see Supporting Youth: How to Care, Communicate, and Connect in Meaningful Ways by Nancy Tellet-Royce.</p>
