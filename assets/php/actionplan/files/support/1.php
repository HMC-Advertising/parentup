Show me how to take action:<br>
Start family traditions and rituals such as family service, game nights, season outings, or family meetings.<br>
Give kids space and respect their privacy when they need it.<br>
Give each of your kids a hug today, even if they’re really big kids.<br>
Spend time each week with each of your teenagers individually.<br>
Create a small memory book, memory box, photo album, or private Web site for each of your children.<br>
If you don’t live in the same city as your child, create a care package that includes a pack of cards; a book of crossword puzzles, word jumbles, or drawing activities; and some colorful pens, pencils, or markers.<br>
*For more on this topic, see Supporting Youth: How to Care, Communicate, and Connect in Meaningful Ways by Nancy Tellet-Royce.
