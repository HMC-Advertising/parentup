    <h3>Other Adult Relationships <small> Young person receives support from three or more nonparent adults.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><p>



Swap a CD or MP3 player with a teen. Listen to the music together if you can, and tell each other why you picked that music.</p><p>
Find a gourmet goodie buddy. Bake brownies, cookies, or other treats with a young person as a fun way to spend time together.</p><p>
Send cards or e-mail greetings to young people you know to mark the holidays, birthdays, and other important milestones in their lives.</p><p>
As a way to spend time together, invite a young friend to till, plant, and tend a garden patch or create a container garden with potted plants.</p><p>
It’s not too late to identify a caring adult who can help take some of the pressure off you during the teenage years. Do you know someone you can bring into a mentoring relationship with your teen who shares your teen’s passion? A colleague? Music instructor? Your child’s employer?</p><p>
*For more on this topic, see Mentoring for Meaningful Results and Connect 5: Finding the Caring Adults You May Not Realize Your Teen Needs by Kathleen Kimball-Baker.</p></div></div>
