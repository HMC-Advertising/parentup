    <h3>Responsibility<small> Young person accepts and takes personal responsibility.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><ul><li>
Don’t always bail your kids out of trouble. Help them learn from mistakes.</li><li>
Don’t nag or rescue your kids when they forget to follow through on a responsibility. Let natural consequences occur (eg, kids who don’t put their dirty clothes in the laundry basket run out of clean clothes to wear).</li><li>
Talk about the cost of things you buy and how you make decisions about what to spend.</li><li>
If you have a pet, encourage your child to take on more responsibility for pet care as he or she matures. If your child is interested in getting a pet, work with her or him to do plenty of research on the care needed for the type of animal you are considering.</li></ul><p>
*For more on this topic, see <em>Teaching Kids to Change the World: Lessons to Inspire Social Responsibility for Grades 6-12</em> by Jennifer Griffin-Wiesner and Chris Maser.</p></div></div>
