<div>
    <h3>Responsibility<small> Young person accepts and takes personal responsibility.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><p>
Don’t always bail your kids out of trouble. Help them learn from mistakes.
Don’t nag or rescue your kids when they forget to follow through on a responsibility. Let natural consequences occur (eg, kids who don’t put their dirty clothes in the laundry basket run out of clean clothes to wear).</p><p>
Talk about the cost of things you buy and how you make decisions about what to spend.</p><p>
If you have a pet, encourage your child to take on more responsibility for pet care as he or she matures. If your child is interested in getting a pet, work with her or him to do plenty of research on the care needed for the type of animal you are considering.</p><p>
*For more on this topic, see Teaching Kids to Change the World: Lessons to Inspire Social Responsibility for Grades 6-12 by Jennifer Griffin-Wiesner and Chris Maser.</p></div></div>
