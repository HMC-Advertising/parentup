    <h3>Equality and Social Justice<small> Young person places high value on promoting equality and reducing hunger and poverty.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><ul><li>
Together, choose an organization or group you want to support financially. Spend six months saving and then deliver your donation in person, if possible.</li><li>
Begin teaching your child the importance of thinking of others who might not have as much as they do. Encourage your kids to “hand down” items they no longer use to charitable organizations.</li><li>
Identify people (past or present) who have worked for social justice. Discuss their impact on their community or the world.</li></ul><p>
