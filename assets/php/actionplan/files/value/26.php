    <h3>Caring<small> Young Person places high value on helping other people.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><p>
Encourage your kids to donate coins—their own or ones you give them—to good causes.</p><p>
Investigate volunteer opportunities in your community that you and your teen can do together, such as stocking food supplies at your local food shelf.</p><p>
Affirm your teenager when he or she acts in ways that are caring or responsible.</p><p> Teenagers need to hear that you’re proud of them and that they are making good choices (even when you’re not happy with all their choices).</p></div></div><div class="page-break"></div>
