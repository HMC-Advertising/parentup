Show me how to take action
Encourage your kids to donate coins—their own or ones you give them—to good causes.
Investigate volunteer opportunities in your community that you and your teen can do together, such as stocking food supplies at your local food shelf.
Affirm your teenager when he or she acts in ways that are caring or responsible. Teenagers need to hear that you’re proud of them and that they are making good choices (even when you’re not happy with all their choices).
