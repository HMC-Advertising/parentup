    <h3>Planning and Decision Making<small> Young person knows how to plan ahead and make choices.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><ul><li>
When your children receive long-term school assignments, offer to help them plan and make decisions in order to finish on time.</li><li>
Talk children through planning ahead by asking “what if” questions. This will help them think about what needs to be done and identify possible consequences of their decisions.</li><li>
Encourage your teen to get involved in a long-term project (one that involves planning and coordination) at school or in the community.</li><li>
Be intentional about letting kids make plans for a family party or event. Let them help make guest lists, plan the budget, shop for food and decorations, and enlist family members to make the event a success.</li></ul><p>
Provide your children with daily planners to help them organize their homework assignments, tests, and after-school activities.</p></div></div><div class="page-break"></div>
