<div>
    <h3>Positive View of Personal Future <small> Young person is optimistic about her or his personal future.</small></h3>
    <div class="content">
    <p><strong>
Show me how to take action:</strong></p><p>
Encourage your children to spend time in their high school guidance office, reading through career and college planning materials.</p><p>
Talk to your teenagers about how they feel about themselves and what they envision for their future.</p><p>
If your teenager is passionate about animals, encourage her or him to consider education or career paths that involve animals when she or he considers post-high school opportunities. There are many.</p><p>
Ask your kids about their goals and dreams. Help them think about the resources (financial and otherwise) they will need to make these goals a reality.</p><p>
*For more on this topic, see Just When I Needed You: True Stories of Adults Who Made a Difference in the Lives of Young People by Deborah Fisher.</p></div></div><div class="page-break"></div>
